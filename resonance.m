% Compute correlations and resonances for prototype chaotic systems:
%   1. Renyi's map (Bernoulli -> beta = 2)
%   
% Future maps to check out:
%   2. Baker's map
%   3. Cat map
%   4. Lorenz
%   5. Rossler
%   6. Dissipative systems
%
%   The idea is to use AAA or REFit to compute resonances via analytic
%   continuation and compare with other approaches.


%% Blaschke map 

mu = -0.33*exp(2*pi/200*j)

b_complex = @(z) ((z - mu) .* (z - mu)) ./ ((1 - conj(mu)*z) .* (1 - conj(mu)*z))
f = @(theta) angle( b_complex( exp(2*pi*j*theta) ) ) / (2*pi) + 1/2

kappa = 0.9311625603 + 0.0974629293*j       % Resonances are powers of this
lambda_true = reshape( [kappa.^(0:60) kappa'.^(1:60)], [], 1 )

figure(4), hold on
plot(real(lambda_true),imag(lambda_true),'ob'), hold on
axis equal
hold off



%% Renyi map
beta = 2;                                   % Resonances at 1/beta^m
f = @(x) mod(beta*x,1);                     % Bernoulli map


%% observables

%m = 2;                                     % Leg. poly. of degree m
%e0 = legpoly(m,[0 1]);                     % will pick out Renyi resonances
%em = legpoly(m,[0 1]);                     % with |z| = 1/beta^m
%e0 = chebfun(@(x) 1+x+x.^2+x.^3+x.^4);
%em = e0;

m = 3                                       % m'th Fourier mode is 
e0 = @(x) cos(2*pi*m*x)                     % a resonant observable
em = e0                                     % (i think, check this l8r!)

npts = 4e7;                                 
%[x,w] = chebpts(npts,[0 1],2);              % Clenshaw-Curtis quadrature
x = linspace(0,1,npts);                      % Fourier quadrature
w = 1/npts;


n = 14;                                     % number of moments/Fourier modes of power spectrum
xk = x;                                     % map gridpts forward
corr = zeros(n+1,1);
for k = 1:n+1
    corr(k) = w.*(e0(xk)*em(x)');            % compute correlation
    xk = f(xk);                             % advance dynamical state
end

